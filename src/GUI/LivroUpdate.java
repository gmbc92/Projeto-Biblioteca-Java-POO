    private void jButtonUpdateLivroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateLivroActionPerformed
        try {
            Livro livro = new Livro();
            NegocioLivro negocio = new NegocioLivro();

            livro.setAutor(jTextFieldAutor.getText());
            livro.setCod_livro(jTextFieldCodigoLivro.getText());
            livro.setTitulo(jTextFieldTitulo.getText());
            livro.setGenero(jTextFieldGenero.getText());
            livro.setEditora(jTextFieldEditor.getText());
            livro.setQuantidade(Integer.parseInt(jTextFieldQuantidade.getText()));
            livro.setAutor(jTextFieldAutor.getText());
            negocio.updateLivro(livro);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Update do Livro na GUI ok");
        }
    }//GEN-LAST:event_jButtonUpdateLivroActionPerformed

